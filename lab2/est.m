function [s1, s2, s3] = est (X)

[K, N] = size(X);

i_k = [2, 3, 4, 5, 6, 7, 8, 9, 10, 15, 20];
a_k = [1.2530, 1.1284, 1.0854, 1.0638, 1.0510, 1.0423, 1.0363, 1.0317, 1.0281, 1.0180, 1.0133];
b_k = [0.8865, 0.5907, 0.4857, 0.4300, 0.3946, 0.3698, 0.3512, 0.3367, 0.3249, 0.2880, 0.2677];
c_k = [1.1829, 1.0646, 1.0374, 1.0260, 1.0201, 1.0161, 1.0136, 1.0116, 1.0103, 1.0063, 1.0046];

a = interp1(i_k, a_k, [2:20]);
b = interp1(i_k, b_k, [2:20]);
c = interp1(i_k, c_k, [2:20]);

s_sr = 0;
r_sr = 0;
med = zeros(N, 1);

for i = 1:N
    s_sr = s_sr + std(X(:, i));
    r_sr = r_sr + (abs(max(X(:, i)) - min(X(:, i))));
    med(i) = std(X(:, i));
end

s_sr = s_sr / N;
r_sr = r_sr / N;
med = median(med);

s1 = a(K) * s_sr
s2 = b(K) * r_sr
s3 = c(K) * med

end